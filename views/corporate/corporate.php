					<h5 class="green-text text-darken-3" >KUMASI AIRPORT</h5><p>
					Although approval was obtained as far back as 1940, it was not until 1947 that documentation for land acquisition was completed. However by 1943, Kumasi Airport had been completed. The runway was 1200yds (3600ft) long by 150yds (450ft) wide.
					The Department of Civil Aviation emerged immediately after the war. The P.W.D constructed a Control Tower, Terminal Building, RFFS station and Staff Quarters which covered an area of 2-3acres. Additionally, a wireless station which took care of all radios was constructed at Dichemso at a cost of £ 1,876.16 (Source: PWD, Kumasi Records)
					<br><br>
					Subsequently, between 1958 and 1959, the Government of Ghana undertook extensive development by upgrading the runway, providing navigational facilities and human resources to enhance internal airline operations. 
					The installation of runway and apron lights and the extension of the main runway in the late 1970's was another major improvement to the Kumasi Airport. Whilst the lights enhanced night flying, the extension of the runway enabled operations of short haul jets like the F28.
					<br><br>
					The building of a new Terminal and the installation of a VOR/DME commissioned on 1st December 1993 completed that phase of the modernization process.
					In 2003 Kumasi was declared an International Airport primarily for regional operations with the full complement of Security, Customs and Immigration facilities and services.
