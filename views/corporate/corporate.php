					</p><br><h5 class="green-text text-darken-3">TAMALE AIRPORT</h5><p>

					Tamale Airport was founded as a main advanced operational base for troops during World War II. The landing strip was acquired at Nyohene, some two miles west of Tamale in December, 1940.
					<br>
					By 1948 the following buildings were in place and ready for operational use:
					<ul style="padding-left: 7%">
						<li class="disc">Aerodrome Control Tower</li>
						<li class="disc">Rescue and Fire Fighting Service (RFFS)</li>
						<li class="disc">Air Traffic Control Offices</li>
						<li class="disc">Standby generator services</li>
						<li class="disc">Public places of convenience</li>
						<li class="disc">Store and garage</li>
						<li class="disc">Seven Junior Staff Quarters were also constructed close to the airport for ease of access by staff</li>
					</ul>
					<br>
					The Government of the first republic of Ghana approved a plan to build a new international airport to serve the military eleven miles north of Tamale on realizing its strategic position/importance. Although the project got off to a bright start, it was abandoned with the overthrow of the regime in 1966. 
					<br><br>
					However, work on the airport resumed with some modifications to accommodate civil operations. Initially, the airport was manned by the Airforce, however with time assistance was sought from GCAA to provide personnel for telecommunications and to take charge of the RFFS and also Air Traffic Services. Tamale Airport is used for both civil and military operations and for special international flights and is now under the management of the GACL.
