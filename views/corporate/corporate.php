					</p><br><h4 class="green-text text-darken-3">SUNYANI AIRPORT</h4><p>
					Sunyani Airport dates back as 1942, when a communication outpost and aerodrome was built for the use of Allied Forces. However, before the end of the war, the airstrip was abandoned.
					In 1969, the Busia Government seeing the need for an airport for Sunyani initiated construction work for a full airport. This was completed and officially opened on 13th July 1974, by Col. P.K. Agyekum, the then commissioner for Transport and Communications.
					The 1,400 metre runway of Sunyani Airport cannot be extended due to a gully at one end and a hill at the other. This limitation renders the airport unsuitable for use by medium range jets. Sunyani airport is one of the domestic airports under the management of the GACL.
